clear all; close all; clc;

% -------------------------------------------------------------------------
% Gamma映射函数
x = [0:1:255];
y1 = x;
y2 = (255/255.^2.2)*x.^2.2;
y3 = (255/255.^(1/2.2))*x.^(1/2.2);
%对比度增强曲线
THRESHOLD = 127;E1 = 4;
y4 = (1./(1 + (THRESHOLD./x).^E1)) * 255;
plot(x,y1,':',x,y2,'-',x,y3,'--',x,y4,'.','Linewidth',2);grid on;
legend('r=1','r=2.2','r=1/2.2','E=4');
xlabel('原始像素');
ylabel('映射后像素');
title('Gamma/对比度增强曲线');
